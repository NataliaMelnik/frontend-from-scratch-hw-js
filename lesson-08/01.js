function capitalizeWords(str) {
  
  let arr = str.split(' ');
  let result = [];
  
  for (let i=0; i<arr.length; i++) {
      let word = arr[i].split('');
      word[0] = word[0].toUpperCase();
      word = word.join('');
      result.push(word);
            
  }
  
  let res = result.join(' ');
   
  return res;
}

capitalizeWords('hello world from javascript');